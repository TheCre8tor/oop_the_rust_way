pub trait Draw {
    fn draw(&self);
}

// Screen ->
pub struct Screen {
    pub components: Vec<Box<dyn Draw>>,
}

impl Screen {
    pub fn run(&self) {
        for component in self.components.iter() {
            component.draw();
        }
    }
}

// Button ->
pub struct Button {
    pub width: u32,
    pub height: u32,
    pub label: String,
}

